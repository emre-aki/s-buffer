#!/bin/bash

cd "$(dirname "$0")"

TEST_ROOT="$(pwd -P)"

# ==============================================================================
# build s-buffer
# ==============================================================================
cd "$TEST_ROOT/.."

./build.sh -d

# ==============================================================================
# build the test suite
# ==============================================================================
cd "$TEST_ROOT"

gcc -o ./test ./test.c -I.. -L../dist -lsbuffer -g

# ==============================================================================
# run the test suite
# ==============================================================================
LD_LIBRARY_PATH=../dist ./test
